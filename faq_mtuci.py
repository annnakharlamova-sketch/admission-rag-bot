# ===== НОВАЯ ФУНКЦИЯ ПОИСКА ПО FAQ (ТОЛЬКО ТОЧНЫЕ СОВПАДЕНИЯ) =====
def search_faq(query):
    """
    Ищет ответ на вопрос в базе FAQ.
    Отвечает ТОЛЬКО если вопрос очень похож на один из FAQ.
    Возвращает (ответ, нашлось_ли_чтото)
    """
    query_clean = query.lower().strip()
    # Убираем знаки препинания в конце
    query_clean = query_clean.rstrip('?.,!;:')
    
    best_match = None
    best_score = 0
    
    for item in FAQ:
        faq_question = item["question"].lower()
        
        # Полное совпадение
        if query_clean == faq_question:
            return item["answer"], True
        
        # Проверяем, содержится ли вопрос FAQ в запросе (или наоборот)
        if faq_question in query_clean or query_clean in faq_question:
            # Считаем длину совпадения
            match_len = max(len(faq_question), len(query_clean))
            score = match_len / min(len(faq_question), len(query_clean))
            
            if score > best_score and score > 0.7:  # 70% похожести
                best_score = score
                best_match = item
    
    if best_match:
        return best_match["answer"], True
    
    return None, False


